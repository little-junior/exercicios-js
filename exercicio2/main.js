function mudarDeCor(cor) {
  document.querySelector("body").style.backgroundColor = cor;
}

function onmouseLaranja() {
  mudarDeCor("orange");
}

function onmouseCinza() {
  mudarDeCor("grey");
}

function onmouseRoxo() {
  mudarDeCor("purple");
}

function onmouseRosa() {
  mudarDeCor("pink");
}

function fundoBranco() {
  mudarDeCor("white");
}
